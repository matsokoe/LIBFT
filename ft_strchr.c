#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*buf;

	i = 0;
	buf = (char *)s;
	while (buf[i] != c)
	{
		if (buf[i] == '\0')
			return (0);
		i++;
	}
	return (&buf[i]);
}
