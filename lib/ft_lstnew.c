#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list		*new;

	if ((new = (t_list *)malloc(sizeof(t_list))) == NULL)
		return (NULL);
	if (content == NULL)
	{
		new->content = NULL;
		new->content_size = 0;
		new->next = NULL;
		return (new);
	}
	else
	{
		new->content = ft_memcpy(ft_memalloc(content_size + 1), content,
				content_size);
		new->content_size = content_size;
	}
	new->next = NULL;
	return (new);
}
