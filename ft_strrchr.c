#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i])
		i++;
	while (s[--i] != c)
		if (s[i] == s[0])
			return (0);
	return ((char *)&s[i]);
}
